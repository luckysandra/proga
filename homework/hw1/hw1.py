a = int(input("Введите число a: "))
b = int(input("Введите число b: "))
c = int(input("Введите число c: "))
if a+b==c:
    print("a и b дают в сумме c, числа обладают свойством (1)")
else:
    print("a и b не дают в сумме c, числа не обладают свойством (1)")
if a/b==c:
    print("при делении a на b получится число c, числа обладают свойством (5)")
else:
    print("при делении a на b число c не получится, числа не обладают свойством (5)")
