a = int(input("Введите число a: "))
b = int(input("Введите число b: "))
c = int(input("Введите число c: "))
if a+b==c:
    print("a и b дают в сумме c")
else:
    print("a и b не дают в сумме c")
if a/b==c:
    print("при делении a на b получится число c")
else:
    print("при делении a на b число c не получится")
